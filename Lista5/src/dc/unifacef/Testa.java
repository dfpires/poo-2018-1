    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String vNome = nome.getText();
        String vMatricula = matricula.getText();
        float vSalarioBase = Float.parseFloat(salarioBase.getText());
        
        if (gerente.isSelected()){
            Gerente objGerente = new Gerente(vNome, vMatricula, vSalarioBase);
            // adiciona gerente
            funcionarios.add(objGerente);
        }
        else if (assistente.isSelected()){
            Assistente objAssistente = 
            new Assistente(vNome, vMatricula, vSalarioBase);
            // adiciona gerente
            funcionarios.add(objAssistente);
        }
        else {
            float vComissao = Float.parseFloat(comissao.getText());
            Vendedor objVendedor = 
            new Vendedor(vComissao, vNome, vMatricula, vSalarioBase);
            // adiciona gerente
            funcionarios.add(objVendedor);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        float folha = 0;
        for(Funcionario funcionario: this.funcionarios){
            folha = folha + funcionario.calculaSalario();
        }
        JOptionPane.showMessageDialog(null, folha);
    }//GEN-LAST:event_jButton2ActionPerformed

